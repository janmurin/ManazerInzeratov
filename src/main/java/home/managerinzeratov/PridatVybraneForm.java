    private void odobratVybraneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_odobratVybraneButtonActionPerformed
        if (vybraneMestaList.getSelectedIndex() != -1) {
            String vybrane = (String) vybraneMestaList.getSelectedValue();
            vybraneMesta.remove(vybraneMestaList.getSelectedIndex());
            vybraneMestaList.setModel(new MestaListModel(vybraneMesta));
            if (vybraneMesta.size() > 0) {
                vybraneMestaList.setSelectedIndex(0);
            }

            vsetkyMesta.add(vybrane);
            Collections.sort(vsetkyMesta);
            vsetkyMestaList.setModel(new MestaListModel(vsetkyMesta));
            vsetkyMestaList.setSelectedIndex(0);
        }
    }//GEN-LAST:event_odobratVybraneButtonActionPerformed

