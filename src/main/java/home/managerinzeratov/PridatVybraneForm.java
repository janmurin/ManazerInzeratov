    public PridatVybraneForm(java.awt.Frame parent, boolean modal, List<String> vsetky, List<String> vybrane, Database database) {
        this(parent, modal);
        // prve nie je mesto lebo je to vsetky mesta
        for (int i = 1; i < vsetky.size(); i++) {
            String akt = vsetky.get(i).split("\\(")[0].trim();
            vsetkyMesta.add(akt);
        }
        
        // prve a posledne nie su mesto
        for (int i = 1; i < vybrane.size() - 1; i++) {
            String akt = vybrane.get(i).split("\\(")[0].trim();
            vybraneMesta.add(akt);
        }
        vybraneMestaList.setModel(new MestaListModel(vybraneMesta));
        // odstranime zo 'vsetkych' nase vybrane mesta
        for (String m:vybraneMesta){
            vsetkyMesta.remove(m);
        }
        vsetkyMestaList.setModel(new MestaListModel(vsetkyMesta));
        vybraneMestaList.setSelectedIndex(0);
        this.database=database;
    }

